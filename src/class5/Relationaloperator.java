package class5;

public class Relationaloperator {
    public static void main(String[] args) {
        int num1 = 10, num2 = 11;

        System.out.println(num1 < num2);  // true
        System.out.println(num1 > num2);  // false
        System.out.println(num1 <= num2); // true
        System.out.println(num1 >= num2); // false
        System.out.println(num1 != num2); // true
        System.out.println(num1 == num2); // false
    }
}
